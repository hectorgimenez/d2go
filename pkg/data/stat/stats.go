package stat

type ID int16

type Data struct {
	ID    ID
	Value int
	Layer int
}

type Stats []Data

func (i Stats) FindStat(id ID, layer int) (Data, bool) {
	for _, s := range i {
		if s.ID == id && s.Layer == layer {
			return s, true
		}
	}

	return Data{}, false
}

